using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;

namespace Content.Client.Humanoid;

[GenerateTypedNameReferences]
public sealed partial class LayerMarkingColorChannel : BoxContainer
{
    public Action<Color>? OnColorChanged;
    public Color LayerColor => ColorSliders.Color;

    public LayerMarkingColorChannel()
    {
        RobustXamlLoader.Load(this);

        ColorSliders.SelectorType = ColorSelectorSliders.ColorSelectorType.Hsv;
        ColorSliders.OnColorChanged += color => { OnColorChanged?.Invoke(color); };
    }

    public void SetLayerText(string text)
    {
        LayerName.Text = text;
    }

    public void SetColor(Color color)
    {
        ColorSliders.Color = color;
    }
}
